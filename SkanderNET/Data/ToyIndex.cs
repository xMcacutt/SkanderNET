    /// <summary>
    /// Generic data about a toy
    /// </summary>
    public class ToyMetaData
    {
        public readonly string Name;
        public readonly Element Element;
        public readonly ToyType Type;
        public readonly ToySubType SubType;
        public readonly VehicleTerrainType VehicleTerrainType;

        internal ToyMetaData(string name, Element element = Element.None, ToyType type = ToyType.Skylander, ToySubType subType = ToySubType.None, VehicleTerrainType terrainType = VehicleTerrainType.None)
        {
            Name = name;
            Element = element;
            Type = type;
            SubType = subType;
            VehicleTerrainType = terrainType;
        }
    }

